//4.3 Write four different Java statements that each add 1 to integer variable x.
 package java_how_to_program_book.ch_4.exercise;

public class Q_3 {
    public static void main(String[] args) {
        int x=0;
        x=x+1;
        System.out.println(x +"\n");
        x=0;
        x+=1;
        System.out.println(x+ "\n");
        x=0;
        x++;
        System.out.println(x+ "\n");
        x=0;
        ++x;
        System.out.println(x+ "\n");



    }
}
